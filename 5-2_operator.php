<?php
    // += 활용
    $num = 10;
    $num += 2;
    echo "[+=사용] 변수 num의 값은 ".$num."<br/>";

    // -= 활용
    $num = 10;
    $num -= 2;
    echo "[-=사용] 변수 num의 값은 ".$num."<br/>";

    // *= 활용
    $num = 10;
    $num *= 2;
    echo "[*=사용] 변수 num의 값은 ".$num."<br/>";

    // /= 활용
    $num = 10;
    $num /= 2;
    echo "[/=사용] 변수 num의 값은 ".$num."<br/>";

    // %= 활용
    $num = 10;
    $num %= 2;
    echo "[%=사용] 변수 num의 값은 ".$num."<br/>";

    // .= 활용
    $num = "서울";
    $num .= "특별시";
    echo "[.=사용] 변수 num의 값은 ".$num."<br/>";